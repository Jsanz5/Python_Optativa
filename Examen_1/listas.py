lista = [10,15,20,25,30] #creamos una lista con 5 elementos
print("Lista : ", lista)

lista.append(35) #agregamos un elemento al final de la lista
print("Elemento agregado: ", lista)

lista.remove(15) #eliminamos un elemento de la lista
print("Elemento eliminado: ", lista)

lista.pop() #eliminamos el último elemento de la lista
print("Último elemento eliminado: ", lista)